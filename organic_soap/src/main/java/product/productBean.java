package product;

public class productBean {
	
	private int pro_key;
	private String pro_img ="";
	private String pro_name ="";
	private int pro_price;
	private String pro_cate ="";
	private String pro_link ="";
	
	public int getPro_key() {
		return pro_key;
	}
	public void setPro_key(int pro_key) {
		this.pro_key = pro_key;
	}
	public String getPro_img() {
		return pro_img;
	}
	public void setPro_img(String pro_img) {
		this.pro_img = pro_img;
	}
	public String getPro_name() {
		return pro_name;
	}
	public void setPro_name(String pro_name) {
		this.pro_name = pro_name;
	}
	public int getPro_price() {
		return pro_price;
	}
	public void setPro_price(int pro_price) {
		this.pro_price = pro_price;
	}
	public String getPro_cate() {
		return pro_cate;
	}
	public void setPro_cate(String pro_cate) {
		this.pro_cate = pro_cate;
	}
	public String getPro_link() {
		return pro_link;
	}
	public void setPro_link(String pro_link) {
		this.pro_link = pro_link;
	}
		
}