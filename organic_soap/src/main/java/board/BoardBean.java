package board;

public class BoardBean {
	
	private int board_key;
	private String board_subject;
	private String board_content;
	private int board_count;
	private String board_date;
	private String board_file_name;
	private String board_write;
	
	
	public int getBoard_key() {
		return board_key;
	}
	public void setBoard_key(int board_key) {
		this.board_key = board_key;
	}
	public String getBoard_subject() {
		return board_subject;
	}
	public void setBoard_subject(String board_subject) {
		this.board_subject = board_subject;
	}
	public String getBoard_content() {
		return board_content;
	}
	public void setBoard_content(String board_content) {
		this.board_content = board_content;
	}
	public int getBoard_count() {
		return board_count;
	}
	public void setBoard_count(int board_count) {
		this.board_count = board_count;
	}
	public String getBoard_date() {
		return board_date;
	}
	public void setBoard_date(String board_date) {
		this.board_date = board_date;
	}
	public String getBoard_file_name() {
		return board_file_name;
	}
	public void setBoard_file_name(String board_file_name) {
		this.board_file_name = board_file_name;
	}
	public String getBoard_write() {
		return board_write;
	}
	public void setBoard_write(String board_write) {
		this.board_write = board_write;
	}

	
	
}